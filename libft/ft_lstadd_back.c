#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tonew;

	if (lst != NULL && new != NULL)
	{
		if (*lst != NULL)
		{
			tonew = ft_lstlast(*lst);
			tonew->next = new;
		}
		else
		{
			*lst = new;
		}
	}
}
